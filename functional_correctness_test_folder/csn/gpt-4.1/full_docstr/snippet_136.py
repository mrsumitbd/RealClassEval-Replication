
import random
import time


class TraceId:
    '''
    A trace ID tracks the path of a request through your application.
    A trace collects all the segments generated by a single request.
    A trace ID is required for a segment.
    '''

    def __init__(self):
        '''
        Generate a random trace id.
        '''
        # Format: 1-<8 hex digits>-<24 hex digits>
        epoch = int(time.time())
        rand_bits = random.getrandbits(96)
        self._id = f"1-{epoch:08x}-{rand_bits:024x}"

    def to_id(self):
        '''
        Convert TraceId object to a string.
        '''
        return self._id
